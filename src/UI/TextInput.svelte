<script>
  export let type = "text";
  export let id;
  export let label;
  export let rows = 3;
  export let value;
  export let valid = true;
  export let errorMessage = "";

  let touched = false;
</script>

<style>
  input,
  textarea {
    display: block;
    width: 100%;
    font: inherit;
    border: none;
    border-bottom: 2px solid #ccc;
    border-radius: 3px 3px 0 0;
    background: white;
    padding: 0.15rem 0.25rem;
    transition: border-color 0.1s ease-out;
  }

  input:focus,
  textarea:focus {
    border-color: #e40763;
    outline: none;
  }

  label {
    display: block;
    margin-bottom: 0.5rem;
    width: 100%;
  }

  .form-control {
    padding: 0.5rem 0;
    width: 100%;
    margin: 0.25rem 0;
  }

  .invalid {
    border: 1px solid #c70039;
    background-color: rgba(199, 0, 57, 0.2);
  }

  .error-message {
    color: #c70039;
    margin: 0.25rem 0;
  }
</style>

<div class="form-control">
  <label for={id}>{label}</label>
  {#if type === 'textarea'}
    <textarea
      class:invalid={!valid && touched}
      {rows}
      {id}
      {value}
      on:input
      on:blur={() => (touched = true)} />
  {:else}
    <input
      class:invalid={!valid && touched}
      {type}
      {id}
      {value}
      on:input
      on:blur={() => (touched = true)} />
  {/if}
  {#if errorMessage && !valid && touched}
    <p class="error-message">{errorMessage}</p>
  {/if}
</div>
